operador = input("digite o operador:\n")

if operador == "+":
    number1 = int(input("num1:\n"))
    number2 = int(input("num2:\n"))
    print (number1 + number2)
elif operador == "-":
    number1 = int(input("num1:\n"))
    number2 = int(input("num2:\n"))
    print (number1 - number2)
elif operador == "*":
    number1 = int(input("num1:\n"))
    number2 = int(input("num2:\n"))
    print (number1 * number2)
elif operador == "/":
    number1 = int(input("num1:\n"))
    number2 = int(input("num2:\n"))
    print (number1 / number2)
else:
    print ("Oloooooco")
